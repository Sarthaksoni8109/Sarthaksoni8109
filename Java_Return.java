package Methods;

public class Java_Return {
    static int myMethod(int x) {
        return 267 + x;
    }

    public static void main(String[] args) {
        System.out.println(myMethod(67));

        //two parameters;
        System.out.println(myMethod(5, 3));
    }

    static int myMethod(int v, int w) {
        return v + w;

    }
}




