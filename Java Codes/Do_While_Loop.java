package example;

public class Do_While_Loop{
    public static void main(String[] args) {

            /*The Do/While Loop;

                        Syntax;
             do {
                 // code block to be executed
               }
                  while (condition); */

        int i = 1;
        do {
            System.out.println(i);
            i++;
        }
        while (i < 10);

    }
}
